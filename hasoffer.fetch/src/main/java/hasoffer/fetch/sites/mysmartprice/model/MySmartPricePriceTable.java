package hasoffer.fetch.sites.mysmartprice.model;

public class MySmartPricePriceTable {

	private String pricetable;

	private String bestprice;
	private String onlineprice;
	private String offlineprice;
	private String mainImageText;
	private String productThumbWrap;
	private String buybutton;
	private String discount;

	public String getPricetable() {
		return pricetable;
	}

	public void setPricetable(String pricetable) {
		this.pricetable = pricetable;
	}

	public String getBestprice() {
		return bestprice;
	}

	public void setBestprice(String bestprice) {
		this.bestprice = bestprice;
	}

	public String getOnlineprice() {
		return onlineprice;
	}

	public void setOnlineprice(String onlineprice) {
		this.onlineprice = onlineprice;
	}

	public String getOfflineprice() {
		return offlineprice;
	}

	public void setOfflineprice(String offlineprice) {
		this.offlineprice = offlineprice;
	}

	public String getMainImageText() {
		return mainImageText;
	}

	public void setMainImageText(String mainImageText) {
		this.mainImageText = mainImageText;
	}

	public String getProductThumbWrap() {
		return productThumbWrap;
	}

	public void setProductThumbWrap(String productThumbWrap) {
		this.productThumbWrap = productThumbWrap;
	}

	public String getBuybutton() {
		return buybutton;
	}

	public void setBuybutton(String buybutton) {
		this.buybutton = buybutton;
	}

	public String getDiscount() {
		return discount;
	}

	public void setDiscount(String discount) {
		this.discount = discount;
	}
}
